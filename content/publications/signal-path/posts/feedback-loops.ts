export const feedbackLoops = `# A practical guide to shorter feedback loops

Fast feedback is not the same as rushed work. The goal is to surface uncertainty while changes are still inexpensive.

## Make the question explicit

Every review should name the decision it is trying to improve. “Thoughts?” creates broad commentary; a precise question creates evidence you can use.

## Reduce batch size

Share the smallest coherent slice that tests the riskiest assumption. A prototype, a query result, or a single user flow can be more informative than a polished package.

## Close the loop

Record what changed and why. People are more likely to offer thoughtful feedback when they can see how earlier input affected the work.`
