export const decisionRecords = `# Decision records for small teams

Decision records preserve the reasoning that disappears from chat threads and meeting notes. They are most valuable when they stay short enough to maintain.

## A durable format

- Context: what changed or became difficult?
- Decision: what will the team do?
- Consequences: what tradeoffs are now accepted?
- Review date: what evidence would reopen the choice?

Write the record when the decision is made, not after the details become folklore. Link to evidence, but keep the core argument readable without opening five other documents.`
